    private void save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save1ActionPerformed
        if(!jList1.isSelectionEmpty())
            try {
                if(jFileChooser1.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                    save = new BufferedWriter(new FileWriter(jFileChooser1.getSelectedFile().getName()));
                    save.write(bigdata.get(jList1.getSelectedIndex()));
                    save.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_save1ActionPerformed

    private void load1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_load1ActionPerformed
            try {
                if(jFileChooser1.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
                    open = new BufferedReader(new FileReader(jFileChooser1.getSelectedFile().getName()));
                    String newFile = null;
                    StringBuilder sb = new StringBuilder();
                    String ls = System.getProperty("line.separator");
                    while((newFile = open.readLine()) != null) {
                        sb.append(newFile);
                        sb.append(ls);
                    }
                    sb.deleteCharAt(sb.length()-1);
                    Title(sb.toString());
                    AddElement(sb.toString(), title);
                    open.close();
                }  
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_load1ActionPerformed
    
    public void Title(String s) { //Определение типа(заголовка) документа(платежка,накладная или заявка на оплату)
        if(s.contains("Количество")) {
            title = String.format("%s от %s номер: %s", invoice1.getText(), sdform.format(new Date()), inv.getNum());
        } else if (s.contains("Сотрудник")) {
            title = String.format("%s от %s номер: %s", payment.getText(), sdform.format(new Date()), p.getNum());
        } else if (s.contains("Комиссия")) {
            title = String.format("%s от %s номер: %s", payment_request1.getText(), sdform.format(new Date()), pr.getNum());
        }
    }
    
    public void ButtonAction(String str, String s) {
        av.set("add");//Определение выборки "Добавление нового док-та" для кнопки "ок"
        jDialog1.setTitle(str);
        jDialog1.setVisible(true);
        jTextArea1.setText(s);
        
    }
    
    public void AddElement(String s1, String t){
        bigdata.add(s1);//Добавление док-та в список данных
        list.addElement(t);//Добавление в лист док-та с заголовком title
    }      
    
